# ==============================
# Поиск максимального числа, кратного 5
# ==============================

n = int(input('Введите количество чисел: '))
l = []  # Список для хранения чисел, кратных 5

for i in range(n):
    num = int(input('Введите число: '))
    if num % 5 == 0:
        l.append(num)  # Добавляем только кратные 5

print(max(l))  # Выводим максимальное число из списка

# Альтернативный способ без списка
n = int(input('Введите количество чисел: '))
max_number = 0  # Переменная для хранения максимального числа

for i in range(n):
    num = int(input('Введите число: '))
    if num % 5 == 0 and num > max_number:
        max_number = num

print(max_number)

# ==============================
# Подсчет суммы чисел, кратных 6
# ==============================

summa = 0  # Переменная для хранения суммы

n = int(input("Введите количество чисел: "))

for i in range(n):
    number = int(input("Введите число: "))
    if number % 6 == 0:
        summa += number  # Добавляем только кратные 6

print(summa)

