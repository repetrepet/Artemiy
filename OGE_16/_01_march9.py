n = int(input("Введите количество чисел: "))
count = 0  # Счетчик чисел, оканчивающихся на 3

for i in range(n):
    number = int(input("Введите число: "))
    if number % 10 == 3:  # Проверяем последнюю цифру
        count += 1  # Увеличиваем счетчик

print(count)

# ==============================
# Подсчет суммы чисел, кратных 6 и оканчивающихся на 4
# ==============================

summa = 0  # Переменная для хранения суммы

while True:
    number = int(input("Введите число (0 - завершение ввода): "))
    if number == 0:
        break  # Выход из цикла при вводе 0
    if number % 6 == 0 and number % 10 == 4:  # Проверяем оба условия
        summa += number  # Добавляем в сумму

print(summa)
