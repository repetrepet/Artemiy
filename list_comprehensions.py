# однострочники

numbers = [1, 2, 3, 4, 5, 6]
# Классический подход: создаём пустой список и последовательно заполняем его результатами цикла
squares = []
for i in numbers:
    squares.append(i ** 2)  # Возводим текущий элемент в квадрат и добавляем в конец списка
print(squares)

# Синтаксис спискового включения (list comprehension) позволяет выполнить ту же операцию лаконичнее
# Общая структура: [выражение for переменная in последовательность]
squares = [i ** 2 for i in numbers]  # Автоматически создаёт новый список, вычисляя квадрат для каждого элемента из numbers
print(squares)

# Создать новый список, где каждое число увеличено на 10.
numbers = [1, 2, 3, 4, 5]
lst = [i + 10 for i in numbers]  # Перебираем исходные числа и сразу формируем список с увеличенными значениями
print(lst)

# Создать список, где все слова записаны заглавными буквами.
words = ["python", "java", "html"]
lst = [i.upper() for i in words]  # Применяем строковый метод upper() к каждому элементу, чтобы перевести буквы в верхний регистр
print(lst)

# Формат спискового включения с фильтрацией: [выражение for переменная in последовательность if условие]
# Взять только четные числа
numbers = [1, 2, 3, 4, 5, 6]  
lst = [i for i in numbers if i % 2 == 0]  # Оператор % возвращает остаток от деления; условие пропускает только чётные числа
print(lst)

# Создать список только из чисел, которые больше 20.
numbers = [10, 15, 20, 25, 30, 35]
lst = [i for i in numbers if i > 20]  # Фильтрация по числовому порогу: в результат попадают значения, строго превышающие 20
print(lst)

# Создать список слов, длина которых больше 3.
words = ["cat", "elephant", "dog", "tiger", "ant"]
lst = [i for i in words if len(i) > 3]  # Функция len() измеряет количество символов; отбираем только длинные слова
print(lst)

# Формат с условным выражением внутри: [значение_если_истина if условие else значение_если_ложь for переменная in последовательность]
numbers = [1, 2, 3, 4, 5]
# ['нечетное', 'четное', 'нечетное', 'четное', 'нечетное']
lst = ['четное' if i % 2 == 0 else 'нечетное' for i in numbers]  # Тернарный оператор заменяет само число на строку в зависимости от чётности
print(lst)

# Дан список чисел:
numbers = [3, -1, 7, -5]

# Создать список, где:
# положительные числа заменяются на "positive";
# отрицательные числа заменяются на "negative";
numbers = [3, -1, 8, 7, -5]
lst = ["positive" if i > 0 else "negative" for i in numbers]  # Проверяем знак: если число больше нуля, подставляем "positive", иначе "negative"
print(lst)

# Дан список:
names = ["anna", "boris", "alex", "dmitry"]
# Создать список имён, которые начинаются на букву "a".
# Ожидаемый результат:
# ["anna", "alex"]
lst = [i for i in names if i[0] == 'a']  # Индексация i[0] извлекает первый символ строки; условие оставляет только подходящие имена
print(lst)
