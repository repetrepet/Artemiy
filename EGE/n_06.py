from turtle import *  # Импортируем все функции из модуля turtle

screensize(3000, 3000)  # Устанавливаем большой размер холста
left(90)                # Поворачиваем черепаху на 90° влево (теперь смотрит вверх)
tracer(0)               # Отключаем анимацию для мгновенной отрисовки
down()                  # Опускаем перо (начинаем рисовать)

size = 20  # Масштабный коэффициент

# Рисуем первый прямоугольник
for i in range(2):
   forward(10 * size)   # Двигаемся вперед на 10*20 = 200 единиц
   right(90)            # Поворачиваем направо на 90°
   forward(18 * size)   # Двигаемся вперед на 18*20 = 360 единиц  
   right(90)            # Поворачиваем направо на 90°

up()    # Поднимаем перо (перестаем рисовать)

# Перемещаемся к началу второго прямоугольника
forward(5 * size)   # Двигаемся вперед на 5*20 = 100 единиц
right(90)           # Поворачиваем направо на 90°
forward(7 * size)   # Двигаемся вперед на 7*20 = 140 единиц
left(90)            # Поворачиваем налево на 90°

down()  # Опускаем перо (снова начинаем рисовать)

# Рисуем второй прямоугольник
for i in range(2):
   forward(10 * size)   # Двигаемся вперед на 10*20 = 200 единиц
   right(90)            # Поворачиваем направо на 90°
   forward(7 * size)    # Двигаемся вперед на 7*20 = 140 единиц
   right(90)            # Поворачиваем направо на 90°

up()    # Поднимаем перо

# Рисуем координатную сетку красными точками
for x in range(-20, 100):
   for y in range(-20, 100):
       setpos(x * size, y * size)  # Перемещаемся в точку (x*20, y*20)
       dot(4, 'red')               # Рисуем красную точку размером 4

done()  # Завершаем работу
