# Текстовый файл состоит не более чем из 10 ^ 6 символов X, Y и Z.
# Определите максимальное количество идущих подряд одинаковых символов.
# Для выполнения этого задания следует написать программу.

# YZYYXZZYZXZXXXXX

# Общая идея решения: Проходим по строке и сравниваем каждый символ со следующим.
# Если символы совпадают, увеличиваем счётчик текущей серии одинаковых символов.
# Если не совпадают, серия прерывается, и счётчик сбрасывается до 1.
# В процессе постоянно отслеживаем максимальное значение счётчика.

f = open('data.txt')
s = f.readline()

count = 1  # Счётчик длины текущей серии одинаковых символов (минимум 1, так как символ уже рассматривается)
mx = 1     # Переменная для хранения максимальной найденной длины серии

# Проходим по строке до предпоследнего символа, чтобы безопасно обращаться к s[index + 1] без выхода за границы
for index in range(len(s) - 1):
    if s[index] == s[index + 1]:
        # Если текущий символ совпадает со следующим, серия продолжается
        count += 1
        # Сразу проверяем, не стала ли текущая серия новой максимальной
        if count > mx:
            mx = count
    else:
        # Если символы различаются, серия прерывается, начинаем подсчёт новой серии с 1
        count = 1

print(mx)


