# Текстовый файл состоит не более чем из 10^6 символов X, Y и Z. Определите максимальную длину цепочки вида XYZXYZXYZ... (составленной из фрагментов XYZ, последний фрагмент может быть неполным).
# Для выполнения этого задания следует написать программу. Ниже приведён файл, который необходимо обработать с помощью данного алгоритма.

# Общая идея решения (Вариант 1): Используем арифметику остатков (modulo).
# Длина текущей цепочки k определяет ожидаемый символ: k%3 == 0 -> 'X', k%3 == 1 -> 'Y', k%3 == 2 -> 'Z'.
# Если символ совпадает с ожидаемым, увеличиваем k. Если нет, но это 'X', начинаем новую цепочку с k=1. Иначе сбрасываем k=0.

f = open('data.txt').readline()
k = 0  # Текущая длина подходящей цепочки
m = 0  # Максимальная длина найденной цепочки

for i in range(len(f)):
    # Проверяем, соответствует ли текущий символ ожидаемому в последовательности XYZ
    if (f[i] == 'X' and k % 3 == 0) or (f[i] == 'Y' and k % 3 == 1) or (f[i] == 'Z' and k % 3 == 2):
        k += 1
        m = max(m, k)  # Обновляем максимум, если текущая цепочка стала длиннее
    elif f[i] == 'X':
        # Если символ не подошёл, но это 'X', он может стать началом новой цепочки
        k = 1
        m = max(m, k)
    else:
        # Любой другой символ, не начинающий цепочку, сбрасывает счётчик
        k = 0

print(m)


# Общая идея решения (Вариант 2): Формируем цепочку посимвольно в строковой переменной.
# Проверяем, можно ли добавить текущий символ к концу уже накопленной цепочки,
# соблюдая строгий порядок следования 'X' -> 'Y' -> 'Z' -> 'X'.

f = open('data.txt')
s = f.readline()

count = 0  # Переменная зарезервирована (в данной реализации длина оценивается через len(chain))
mx = 0     # Максимальная длина найденной цепочки
chain = '' # Строка для накопления текущей подходящей цепочки

for index in range(len(s)):
    if s[index] == 'X' and chain == '':
        # Если цепочка пуста и встретился 'X', начинаем новую цепочку
        chain += 'X'
    elif chain and chain[-1] == 'X' and s[index] == 'Y':
        # Если последний символ 'X', ожидаем 'Y'
        chain += 'Y'
    elif chain and chain[-1] == 'Y' and s[index] == 'Z':
        # Если последний символ 'Y', ожидаем 'Z'
        chain += 'Z'
    elif chain and chain[-1] == 'Z' and s[index] == 'X':
        # Если последний символ 'Z', ожидаем 'X' для продолжения цикла
        chain += 'X'
    else:
        # Если символ нарушает порядок, фиксируем максимум и сбрасываем цепочку
        mx = max(mx, len(chain))
        chain = ''

print(mx)


