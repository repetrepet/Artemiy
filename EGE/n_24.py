# Для выполнения этого задания следует написать программу.

# Общая идея решения: Накапливаем символы в строку chain.
# Если встречается два символа 'P' подряд, текущая цепочка прерывается,
# мы обновляем максимальную длину и начинаем формировать новую цепочку.

f = open('data.txt')
s = f.readline()

count = 0  # Переменная зарезервирована (в данном алгоритме длина оценивается через len(chain))
mx = 0     # Максимальная длина подходящей цепочки
chain = '' # Текущая накапливаемая цепочка символов

# Проходим по строке до предпоследнего символа (особенность исходного алгоритма)
for index in range(len(s) - 1):
    if len(chain) == 0:
        # Если цепочка пуста, инициализируем её текущим символом
        chain += s[index]
    
    # Проверяем условие прерывания: последний символ цепочки и текущий символ оба 'P'
    if chain[-1] == 'P' and s[index] == 'P':
        mx = max(mx, len(chain))
        chain = ''  # Сбрасываем цепочку, так как найдено нарушение "PP"
    else:
        # Если условия прерывания нет, добавляем текущий символ в цепочку
        chain += s[index]

print(mx)




