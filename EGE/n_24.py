# 10^6 заглавных букв английского алфавита. Определите в файле количество непрерывных последовательностей длины 5, являющихся палиндромами.

# Примечание. Палиндром — это слово или фраза, которые одинаково читаются слева направо и справа налево.

# Идея решения: Проходим по строке "окнами" длиной 5 символов. 
# Для каждого окна проверяем, равно ли оно своей перевёрнутой версии (с помощью среза [::-1]).

f = open('data.txt')
s = f.readline()

count = 0  # Счётчик найденных палиндромов длины 5

# Проходим по строке так, чтобы можно было взять полный срез из 5 символов (до len(s) - 4)
for index in range(len(s) - 4):
    string = s[index:index+5]  # Извлекаем подстроку длиной ровно 5 символов
    
    # Сравниваем подстроку с её перевёрнутой копией
    if string == string[::-1]:
        count += 1  # Если они равны, это палиндром, увеличиваем счётчик

print(count)
