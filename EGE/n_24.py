# Идея решения: Проверяем каждую пару соседних символов. Если пара не является 'IT' или 'TI', 
# мы можем продолжить текущую последовательность. Если пара запрещена, фиксируем максимум и начинаем новую последовательность.

f = open('data.txt')
s = f.readline()

count = 0  # Счётчик длины текущей допустимой последовательности
mx = 0     # Максимальная длина допустимой последовательности

for index in range(len(s) - 1):
    # Проверяем, что текущий и следующий символ вместе не образуют запрещённые пары 'IT' или 'TI'
    if s[index] + s[index + 1] not in ['IT', 'TI']:
        count += 1  # Пара допустима, увеличиваем длину текущей последовательности
    else:
        # При встрече запрещённой пары обновляем максимум. 
        # Добавляем 1 к count, так как символ по индексу index всё ещё может быть началом новой допустимой последовательности
        mx = max(count + 1, mx)
        count = 0   # Сбрасываем счётчик для новой последовательности

print(mx)


