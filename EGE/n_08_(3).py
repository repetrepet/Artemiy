# каждому сообщению соответствует своё кодовое слово.
# В качестве кодовых слов Алексей использует 5-буквенные слова,
# в которых есть только буквы A, B, C, X, причём буква X может
# появиться на первом месте или не появиться вовсе.
# Сколько различных кодовых слов может использовать Алексей?


from itertools import product

alph = 'АВСХ'
count = 0

# Требование:
# 1) Либо буквы 'Х' нет вообще (i.count('Х') == 0)
# 2) Либо она встречается ровно один раз и стоит на первой позиции (i[0] == 'Х' и count == 1)
for i in product(alph, repeat=5):
    if (i[0] == 'Х' and i.count('Х') == 1) or i.count('Х') == 0:
        count += 1

print(count)


