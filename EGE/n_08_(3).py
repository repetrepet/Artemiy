# Сколько слов длины 5, начинающихся с гласной буквы, можно
# составить из букв Е, Г, Э? Каждая буква может входить в
# слово несколько раз. Слова не обязательно должны быть осмысленными
# словами русского языка.
from itertools import product  # product(...) перебирает все последовательности (декартово произведение)

# Ниже — решение перебором всех 5-буквенных "слов" из алфавита 'ЕГЭ'
# Идея: всего слов 3^5, но нам нужны только те, где первая буква — гласная (Е или Э).
from itertools import product

alph = 'ЕГЭ'  # алфавит
count = 0     # счётчик подходящих слов

# product(alph, repeat=5) возвращает кортежи длины 5 (по одному символу на позицию)
for i in product(alph, repeat=5):
    # Условие "начинается с гласной" здесь эквивалентно "первая буква НЕ 'Г'"
    # (потому что в алфавите только одна согласная — 'Г')
    if i[0] != 'Г':
        count += 1

print(count)


# Сколько слов длины 5, начинающихся с согласной буквы и
# заканчивающихся гласной буквой, можно составить из букв З, И, М, А?
# Каждая буква может входить в слово несколько раз.
# Слова не обязательно должны быть осмысленными словами русского языка.

from itertools import product

alph = 'ЗИМА'
count = 0

# Согласные из данного набора: 'З', 'М'
# Гласные: 'И', 'А'
for i in product(alph, repeat=5):
    # i[0] — первая буква, i[-1] — последняя буква
    if i[0] in 'ЗМ' and i[-1] in 'ИА':
        count += 1

print(count)



