# Олег составляет таблицу кодовых слов для передачи сообщений, каждому сообщению соответствует
# своё кодовое слово. В качестве кодовых слов Олег использует 4-буквенные слова,
# в которых есть только буквы A, B, C, D, E, X, Z, причём буквы X и Z встречаются
# только на двух первых позициях, а буквы A, B, C, D, E— только на двух последних.
# Сколько различных кодовых слов может использовать Олег?

# Ниже — набросок идеи (оставлен как черновик в комментариях).
# s1 = 'xz'
# s2 = 'abcde'
#
# if (i[0] in s1) and (i[2] in s1) and (i[2] in s2) and

from itertools import product

alph = 'ABCDEXZ'
count = 0
s1 = 'XZ'     # допустимы ТОЛЬКО на позициях 0 и 1
s2 = 'ABCDE'  # допустимы ТОЛЬКО на позициях 2 и 3

# Условие по позициям:
# i[0], i[1] ∈ {X, Z}
# i[2], i[3] ∈ {A, B, C, D, E}
for i in product(alph, repeat=4):
    if (i[0] in s1) and (i[1] in s1) and (i[2] in s2) and (i[3] in s2):
        count += 1

print(count)




