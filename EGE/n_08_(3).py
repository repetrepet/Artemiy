# Запишите слово, которое стоит на 210-м месте от начала списка.

from itertools import product

alph = "АОУ"

# Вариант 1 (учебный): вывести все слова с их номерами.
# Важно: enumerate(...) нумерует с 0, поэтому "человеческий" номер = num + 1.
for num, i in enumerate(product(alph, repeat=5)):
    print(num, i)

# Вариант 2 (практичный): сразу взять 210-е слово.
# Так как индексация списка с 0, то 210-е по счёту имеет индекс 209.
from itertools import product
words = list(product('АОУ', repeat=5))
print(words[209])




