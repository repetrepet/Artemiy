# Функция для подсчета количества программ (траекторий)
def f(number, target):
    # Базовый случай: если достигли нужного числа, значит, нашли 1 верный путь
    if number == target:
        return 1
    # Если число стало меньше целевого, дальнейший путь невозможен (команды только уменьшают)
    if number < target:
        return 0
    # Рекурсивный шаг: суммируем количество путей для каждой из трех команд
    return f(number - 2, target) + f(number - 3, target) + f(number - 4, target)

