# Программа для исполнителя — это последовательность команд.
# Сколько существует программ, для которых при исходном числе 42 результатом является число 89 и при этом траектория содержит число 73 и не содержит числа 81?

# Функция для подсчета количества программ
def f(n, t):
    # Базовый случай: достигли целевого числа
    if n == t:
        return 1
    # Если превысили целевое число или попали в запрещенную точку (81), путь неверный
    if n > t or n == 81:
        return 0
    # Рекурсивный шаг: 
    # 1) прибавляем старший разряд (переводим в строку, берем первый символ и обратно в int)
    # 2) прибавляем 3
    # 3) делаем число нечетным (2x - 1)
    return f(int(str(n)[0]) + n, t) + f(n + 3, t) + f(2 * n - 1, t)

# Обязательное прохождение через 73 делит задачу на два независимых отрезка:
# от 42 до 73 и от 73 до 89. Перемножаем количество способов.
print(f(42, 73) * f(73, 89))


# Исполнитель Сумножатор имеет две команды:

# 1. Прибавить 4.
# 2. Умножить на 2.

# Команда 1 увеличивает число на экране на 4, команда 2 — вдвое.

# Сколько различных чисел может получиться при начальном числе 2 в ходе работы исполнителя, если известно, что программа состоит из 5 команд?
# 11111
# 11112
# 11121

# Импортируем функцию product для генерации всех возможных комбинаций команд
from itertools import product

instructions = []

# Генерируем все возможные программы из 5 команд, где каждая команда '1' или '2'
for i in product('12', repeat=5):
    instructions.append(''.join(i))

lst = []

# Проходим по каждой сгенерированной программе и эмулируем её выполнение
for line in instructions:
    number = 2  # Исходное число
    for i in line:
        if i == '1': 
            number += 4  # Команда 1: прибавить 4
        if i == '2': 
            number *= 2  # Команда 2: умножить на 2
    lst.append(number)  # Сохраняем полученный результат

# Выводим список всех полученных чисел для наглядности
print(lst)
print()

# Переводим список в множество (set), чтобы убрать повторяющиеся числа, 
# и считаем количество уникальных результатов
print(len(set(lst)))
