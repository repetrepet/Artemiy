#
# Найдите модуль разности между количеством положительных и отрицательных чисел в файле.

lst = [int(i) for i in open('text.txt')]

# Инициализируем счётчики для положительных и отрицательных элементов
count_pos = 0
count_neg = 0

# Проходим по каждому числу и классифицируем его по знаку
for i in lst:
    if i > 0:
        count_pos += 1
    else:
        # В данной реализации нули учитываются в ветке else (как неотрицательные/отрицательные)
        count_neg += 1

# Выводим абсолютную разницу между накопленными счётчиками
print(abs(count_pos - count_neg))


# B файле содержится последовательность натуральных чисел. Её элементы могут принимать целые
# значения от 1 до 100 000 включительно. Определите количество пар последовательности, в которых
# хотя бы один из элементов является чётным числом, a сумма элементов пары меньше максимального
# четырёхзначного элемента последовательности, заканчивающегося на 86.
#
# В ответе запишите количество найденных пар, затем минимальную из сумм элементов таких пар.
#
# В данной задаче под парой подразумевается два идущих подряд элемента последовательности.

lst = [int(i) for i in open('text.txt')]

# Список для хранения четырёхзначных чисел, оканчивающихся на 86
elements = []

for i in lst:
    # Преобразуем число в строку для проверки количества цифр и последних символов
    if len(str(i)) == 4 and str(i)[-2:] == '86':
        elements.append(i)

# Находим максимальный элемент среди отобранных четырёхзначных чисел, заканчивающихся на 86
max_element = max(elements)
count = 0
sums = []

# Перебираем все соседние пары элементов последовательности
for index in range(len(lst) - 1):
    s1 = lst[index]
    s2 = lst[index + 1]

    # Прямая проверка на чётность: остаток от деления хотя бы одного числа на 2 равен 0
    # Альтернативный способ: произведение двух целых чисел чётно, если хотя бы один из множителей чётен
    # if (s1 * s2) % 2 == 0:
    if s1 % 2 == 0 or s2 % 2 == 0:
        # Проверяем основное условие: сумма пары должна быть строго меньше найденного максимума
        if s1 + s2 < max_element:
            sums.append(s1 + s2)
            count += 1

# Выводим количество подходящих пар и минимальную сумму среди них
print(count, min(sums))


# B файле содержится последовательность натуральных чисел. Её элементы могут принимать целые
# значения от 1 до 100 000 включительно.
#
# Определите количество пар последовательности, в которых хотя бы один из элементов является
# четырёхзначным числом, a сумма элементов пары кратна минимальному трёхзначному элементу
# последовательности, кратному 45.
#
# В ответе запишите количество найденных пар, затем минимальную из сумм элементов таких пар. В
# данной задаче под парой подразумевается два идущих подряд элемента последовательности.

lst = [int(i) for i in open('text.txt')]

# Список для отбора трёхзначных чисел, кратных 45
elements = []

for i in lst:
    # Проверяем, что число состоит ровно из трёх цифр и делится на 45 без остатка
    if len(str(i)) == 3 and i % 45 == 0:
        elements.append(i)

# Находим минимальный элемент среди отобранных, который будет выступать в роли делителя
element = min(elements)
count = 0
sums = []

# Проходим по всем соседним парам
for index in range(len(lst) - 1):
    s1 = lst[index]
    s2 = lst[index + 1]

    # Условие: хотя бы одно число в паре должно быть четырёхзначным
    if len(str(s1)) == 4 or len(str(s2)) == 4:
        # Проверка кратности суммы делителю. 
        # Скобки обязательны: без них оператор % выполнится раньше сложения из-за приоритета операций.
        # Сравнение с 0 гарантирует, что остаток от деления отсутствует.
        if (s1 + s2) % element == 0:
            sums.append(s1 + s2)
            count += 1

print(count, min(sums))
