# В файле содержится последовательность натуральных чисел, каждое из которых не превышает 100
# 000. Определите количество троек элементов последовательности, в которых ровно два из трёх
# элементов являются трёхзначными числами, а сумма элементов тройки не больше максимального
# элемента последовательности, оканчивающегося на 13.
#
# Гарантируется, что в последовательности есть хотя бы одно число, оканчивающееся на 13.
#
# В ответе запишите количество найденных троек чисел, затем максимальную из сумм элементов таких
# троек. В данной задаче под тройкой подразумевается три идущих подряд элемента последовательности.

# Открываем файл для чтения последовательности
f = open('text.txt')

# Считываем все строки файла и преобразуем их в целые числа, сохраняя в список
lst = [int(i) for i in f]

# Отбираем только те числа, которые заканчиваются на 13.
# Преобразуем число в строку и проверяем последние два символа срезом [-2:].
# Из отобранных находим максимальное значение — это будет порог для сравнения сумм.
elements = [i for i in lst if str(i)[-2:] == '13']
element = max(elements)

# Создаём список для накопления валидных сумм троек и счётчик их количества
sums = []
count = 0

# Проходим по индексам списка так, чтобы всегда можно было взять текущий элемент
# и два следующих за ним. range(len(lst) - 2) гарантирует, что выход за границы не произойдёт.
for index in range(len(lst) - 2):
    s1 = lst[index]
    s2 = lst[index + 1]
    s3 = lst[index + 2]

    # Проверяем первое условие: ровно два числа из трёх должны быть трёхзначными.
    # Сравнение возвращает True (1) или False (0). Сумма трёх таких значений показывает,
    # сколько чисел в тройке попадают в диапазон [100; 999]. Сравниваем сумму с 2.
    if ((100 <= s1 <= 999) + (100 <= s2 <= 999) + (100 <= s3 <= 999)) == 2:
        # Проверяем второе условие: сумма элементов тройки не должна превышать порог
        if s1 + s2 + s3 <= element:
            sums.append(s1 + s2 + s3)
            count += 1

# Выводим итоговое количество подходящих троек и максимальную сумму среди них
print(count, max(sums))


# В файле содержится последовательность натуральных чисел, каждое из которых не превышает 100
# 000. Определите количество троек элементов последовательности, в которых не больше двух из трёх
# элементов чётные, а сумма элементов тройки не больше третьего по величине элемента
# последовательности.
#
# В ответе запишите количество найденных троек чисел, затем максимальную из сумм элементов таких
# троек.
#
# В данной задаче под тройкой подразумевается три идущих подряд элемента последовательности.

# Открываем файл для чтения данных
f = open('text.txt')

# Загружаем всю последовательность в список целых чисел
lst = [int(i) for i in f]

# Подготавливаем список для хранения сумм подходящих троек и счётчик
sums = []
# Находим третий по величине элемент в последовательности.
# Функция sorted() возвращает новый отсортированный по возрастанию список.
# Индекс [-3] обращается к третьему элементу с конца, что соответствует третьему максимуму.
element = sorted(lst)[-3]
count = 0

# Перебираем все возможные тройки подряд идущих элементов последовательности
for index in range(len(lst) - 2):
    s1 = lst[index]
    s2 = lst[index + 1]
    s3 = lst[index + 2]

    # Проверяем условие на чётность: в тройке должно быть не больше двух чётных чисел.
    # Выражение `x % 2 == 0` возвращает True (1) для чётных и False (0) для нечётных.
    # Сумма трёх булевых результатов даёт общее количество чётных чисел в тройке.
    if ((s1 % 2 == 0) + (s2 % 2 == 0) + (s3 % 2 == 0)) <= 2:
        # Второе условие: сумма элементов тройки не превышает найденный порог
        if s1 + s2 + s3 <= element:
            sums.append(s1 + s2 + s3)
            count += 1

# Выводим количество найденных троек и максимальную сумму среди них
print(count, max(sums))
