# В ответе запишите количество найденных пар, затем минимальную из сумм элементов таких пар. В
# данной задаче под парой подразумевается два идущих подряд элемента последовательности.

lst = [int(i) for i in open('text.txt')]

# Список для отбора трёхзначных чисел, кратных 45
elements = []

for i in lst:
    # Проверяем, что число состоит ровно из трёх цифр и делится на 45 без остатка
    if len(str(i)) == 3 and i % 45 == 0:
        elements.append(i)

# Находим минимальный элемент среди отобранных, который будет выступать в роли делителя
element = min(elements)
count = 0
sums = []

# Проходим по всем соседним парам
for index in range(len(lst) - 1):
    s1 = lst[index]
    s2 = lst[index + 1]

    # Условие: хотя бы одно число в паре должно быть четырёхзначным
    if len(str(s1)) == 4 or len(str(s2)) == 4:
        # Проверка кратности суммы делителю. 
        # Скобки обязательны: без них оператор % выполнится раньше сложения из-за приоритета операций.
        # Сравнение с 0 гарантирует, что остаток от деления отсутствует.
        if (s1 + s2) % element == 0:
            sums.append(s1 + s2)
            count += 1

print(count, min(sums))



