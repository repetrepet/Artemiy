# B файле содержится последовательность натуральных чисел. Её элементы могут принимать целые
# значения от 1 до 100 000 включительно. Определите количество пар последовательности, в которых
# хотя бы один из элементов является чётным числом, a сумма элементов пары меньше максимального
# четырёхзначного элемента последовательности, заканчивающегося на 86.
#
# В ответе запишите количество найденных пар, затем минимальную из сумм элементов таких пар.
#
# В данной задаче под парой подразумевается два идущих подряд элемента последовательности.

lst = [int(i) for i in open('text.txt')]

# Список для хранения четырёхзначных чисел, оканчивающихся на 86
elements = []

for i in lst:
    # Преобразуем число в строку для проверки количества цифр и последних символов
    if len(str(i)) == 4 and str(i)[-2:] == '86':
        elements.append(i)

# Находим максимальный элемент среди отобранных четырёхзначных чисел, заканчивающихся на 86
max_element = max(elements)
count = 0
sums = []

# Перебираем все соседние пары элементов последовательности
for index in range(len(lst) - 1):
    s1 = lst[index]
    s2 = lst[index + 1]

    # Прямая проверка на чётность: остаток от деления хотя бы одного числа на 2 равен 0
    # Альтернативный способ: произведение двух целых чисел чётно, если хотя бы один из множителей чётен
    # if (s1 * s2) % 2 == 0:
    if s1 % 2 == 0 or s2 % 2 == 0:
        # Проверяем основное условие: сумма пары должна быть строго меньше найденного максимума
        if s1 + s2 < max_element:
            sums.append(s1 + s2)
            count += 1

# Выводим количество подходящих пар и минимальную сумму среди них
print(count, min(sums))


# B файле содержится последовательность натуральных чисел. Её элементы могут принимать целые
# значения от 1 до 100 000 включительно.
