# В файле содержится последовательность целых чисел. Элементы последовательности могут принимать
# целые значения от –10 000 до 10 000 включительно.
#
# Найдите количество пар чисел с одинаковым знаком. Под парой понимается 2 подряд идущих числа.
f = open('text.txt')

# Первый способ чтения: построчный перебор файла с явным преобразованием каждой строки в число
# l = []
# for i in f:
#     l.append(int(i))

# Более компактный и питонический способ: list comprehension автоматически считывает все строки,
# удаляет переводы кареток и преобразует их в целые числа, формируя готовый список
l = [int(i) for i in f]
count = 0

# Проходим по индексам от 0 до len(l) - 2, чтобы безопасно обращаться к l[index + 1]
# без выхода за границы списка
for index in range(len(l) - 1):
    s1 = l[index]
    s2 = l[index + 1]

    # Проверка одинакового знака: оба числа строго больше нуля или оба строго меньше нуля
    # (нуль не имеет знака, поэтому по условию задачи обычно исключается или обрабатывается отдельно)
    # Альтернативный вариант: произведение двух чисел с одинаковым знаком всегда положительно (> 0)
    # if s1 * s2 > 0:
    if (s1 > 0 and s2 > 0) or (s1 < 0 and s2 < 0):
        count += 1

print(count)


# В файле содержится последовательность целых чисел. Элементы последовательности могут принимать
# целые значения от –10 000 до 10 000 включительно.
#
# Найдите модуль разности между самым большим положительным числом и самым маленьким
# отрицательным числом.

# Открываем файл и формируем список чисел
f = open('text.txt')
lst = [int(i) for i in f]

# Встроенные функции max() и min() находят наибольшее и наименьшее значения в последовательности
# В рамках данного алгоритма предполагается, что в файле гарантированно есть как положительные,
# так и отрицательные числа, поэтому глобальные max/min совпадают с искомыми
max_number = max(lst)
min_number = min(lst)

# Функция abs() возвращает модуль числа, что эквивалентно расстоянию между двумя точками на оси
print(abs(max_number - min_number))


# В файле содержится последовательность целых чисел. Элементы последовательности могут принимать
