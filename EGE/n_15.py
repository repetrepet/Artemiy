# Конспект: решение логических задач методом перебора
# Примечание: в Python импликация (→) реализуется через оператор <=, 
# дизъюнкция (∨) через or, конъюнкция (∧) через and, отрицание (¬) через not.

# # ДЕЛ(x,A)→(¬ДЕЛ(x,25)∨ДЕЛ(x,16))
# Пояснение: ищем наименьшее A, при котором выражение истинно для всех x из заданного диапазона.
# Функция func реализует проверку делимости ДЕЛ(x, m).
def func(n, m):
    return n % m == 0


for A in range(1, 10 ** 10):
    if all(
        func(x, A) <= (not func(x, 25) or func(x, 16))
        for x in range(-100, 100)
    ):
        print(A)
        break


# (3y−x>12)∨(2x+6y≥72)∨(x>24)∨(x⋅y<A)
# Пояснение: перебираем A, пока дизъюнкция не станет истинной для всех пар (x, y).
# Выражение истинно, если хотя бы одно из условий выполняется для каждой пары.
for A in range(0, 1000):
    if all(
        (3 * y - x > 12) or (2 * x + 6 * y >= 72) or (x > 24) or (x * y < A)
        for x in range(100)
        for y in range(100)
    ):
        print(A)
        break


# ((x&52!=0)∧(x&48=0))→¬(x&А=0)
# Пояснение: задача на побитовую логику.
# & - побитовое И, <= - логическая импликация.
# Проверяем, что для всех x из диапазона истинно условие.
for A in range(0, 10000):
    if all(
        ((x & 52 != 0) and (x & 48 == 0)) <= (not (x & A == 0))
        for x in range(100)
    ):
        print(A)
        break


# (¬ДЕЛ(x,A)∧(x∈B))→¬ДЕЛ(x,13)
# Пояснение: проверка принадлежности x ∈ B выполняется оператором in.
# Логическая структура: импликация конъюнкции отрицания делимости и принадлежности.
B = range(132, 151)


def f(n, m):
    """Проверка делимости числа n на m (ДЕЛ(n, m))"""
    return n % m == 0


for A in range(2, 10000):
    if all(
        ((not f(x, A)) and (x in B)) <= (not f(x, 13))
        for x in range(0, 1000)
    ):
        print(A)
        break


# ((y+x)^2>1048)∨((x+20<A)∧(40−y<A))
# Пояснение: поиск минимального A, при котором дизъюнкция истинна для всех натуральных x, y ≥ 1.
# Возведение в степень реализуется оператором **.
for A in range(1000):
    if all(
        ((y + x) ** 2 > 1048) or ((x + 20 < A) and (40 - y < A))
        for x in range(1, 1000)
        for y in range(1, 1000)
    ):
        print(A)
        break
