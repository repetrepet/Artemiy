# Обозначим через ДЕЛ(n, m) утверждение «натуральное число n делится без остатка на натуральное
# число m».
# Для какого наименьшего натурального числа A формула (ДЕЛ(x, 12) and ДЕЛ(x, 5)) <= (ДЕЛ(x, A))
# тождественно истинна (то есть принимает значение 1 при любом натуральном значении
# переменной x)?


def func(n, m):
    """
    Функция-предикат ДЕЛ(n, m).
    Возвращает True, если n делится на m без остатка, иначе False.
    """
    return n % m == 0


# Перебираем возможные значения A в поисках наименьшего натурального числа
for A in range(1, 100):
    # Проверяем тождественную истинность формулы для всех x из заданного диапазона.
    # Оператор <= для булевых значений в Python эквивалентен логической импликации (P -> Q):
    # True <= True  -> True
    # True <= False -> False
    # False <= True -> True
    # False <= False-> True
    if all(
            (func(x, 12) and func(x, 5)) <= (func(x, A))
            # Примечание: диапазон включает 0 и отрицательные числа.
            # Для операции взятия остатка и проверки делимости это допустимо
            # и не нарушает условие тождественной истинности импликации.
            for x in range(-100, 100)
    ):
        print(A)  # Выводим первое (наименьшее) подходящее значение A
        break     # Прерываем цикл, так как дальнейший перебор не требуется
