# Алгоритм вычисления значения функции F(n), где n — натуральное число, задан такими соотношениями:
#
# F(n) = 1 при n <= 1,
# F(n) = (n / 2) * F(n - 1), если n > 1 и n чётно,
# F(n) = ((n - 1) / 2) * F(n - 1) иначе.
#
# Чему равно значение выражения
# (F(2024) - F(2022)) / F(2021)?

from sys import setrecursionlimit
setrecursionlimit(10000)  # Увеличиваем лимит глубины рекурсии


def f(n):
    # Базовый случай: для n <= 1 возвращаем 1
    if n <= 1:
        return 1
    # Случай для чётных n > 1: умножаем на n//2
    elif n > 1 and n % 2 == 0:
        return (n // 2) * f(n - 1)
    # Случай для нечётных n: умножаем на (n-1)//2
    else:
        return ((n - 1) // 2) * f(n - 1)

# Вычисляем итоговое выражение с использованием целочисленного деления
print((f(2024) - f(2022)) // f(2021))
