# Алгоритм вычисления значения функции F(n), где n — натуральное число, задан такими соотношениями:
#
# F(n) = n, если n >= 7000
# F(n) = F(n + 2) + n + 3, если n < 7000
#
# Чему равно значение выражения F(52) - F(56)?

from sys import setrecursionlimit
setrecursionlimit(10000)  # Увеличиваем лимит глубины рекурсии для корректной работы


def f(n):
    # Базовый случай: при достижении порога возвращаем само значение
    if n >= 7000:
        return n
    # Рекурсивный случай: восходящая рекурсия с шагом +2
    else:
        return f(n + 2) + n + 3

print(f(52) - f(56))


