#
# F(n) = 1, если n <= 5
# F(n) = n + F(n-2), если n > 5
#
# Чему равно значение выражения F(2126) - F(2122)?

from sys import setrecursionlimit
setrecursionlimit(10000)  # Увеличиваем лимит глубины рекурсии


def f(n):
    # Базовый случай: для n <= 5 возвращаем 1
    if n <= 5:
        return 1
    # Рекурсивный случай: прибавляем текущее n и вызываем функцию с шагом -2
    else:
        return n + f(n - 2)

print(f(2126) - f(2122))


