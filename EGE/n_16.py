# Ниже записана рекурсивная функция:
# Сколько символов «звёздочка» будет напечатано на экране при выполнении вызова f(40)?

count = 0  # Глобальный счётчик напечатанных символов '*'


def f(n):
    global count
    print('*')
    count += 1
    if n >= 1:
        print('*')
        count += 1
        f(n - 1)      # Первый рекурсивный вызов: уменьшение на 1
        f(n // 2)     # Второй рекурсивный вызов: целочисленное деление на 2

# Примечание: f(40) возвращает None, поэтому печатаем только значение счётчика
# print(f(40))  # Закомментировано, т.к. не влияет на результат
