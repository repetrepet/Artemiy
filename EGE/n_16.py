# Алгоритм вычисления значения функции F(n), где n - целое число, задан следующими соотношениями:
# F(n)=1,если n<3
# F(n) = F(n-3)+F(n-2)+F(n-1),если n >= 3
# Чему равно значение выражения F(6)?

def f(n):
    # Базовый случай рекурсии: для n < 3 функция возвращает 1
    if n < 3:
        return 1
    # Рекурсивный случай: сумма трёх предыдущих значений функции
    else:
        return f(n - 3) + f(n - 2) + f(n - 1)

print(f(6))


