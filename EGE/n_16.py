# Алгоритм вычисления значения функции F(n), где n — натуральное число, задан такими соотношениями:
#
# F(n) = 1 при n < 3
# F(n) = F(n - 1) + n - 1 при n > 2 и при этом чётно
# F(n) = F(n - 2) + 2n - 2, если n > 2 и при этом нечётно
#
# Чему равно значение выражения F(3048) - F(3045)?


from sys import setrecursionlimit
setrecursionlimit(10000)  # Увеличиваем лимит глубины рекурсии


def f(n):
    # Базовый случай: для n < 3 возвращаем 1
    if n < 3:
        return 1
    # Случай для чётных n > 2
    elif n > 2 and n % 2 == 0:
        return f(n - 1) + n - 1
    # Случай для нечётных n > 2
    elif n > 2 and n % 2 != 0:
        return f(n - 2) + 2 * n - 2

print(f(3048) - f(3045))


