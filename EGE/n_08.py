# ЗАДАЧА 10: Перестановки и четность (permutations)
# ==========================================

# Определите количество четырехзначных чисел, записанных в десятичной системе
# счисления, в записи которых все цифры различны и никакие две чётные и две
# нечётные цифры не стоят рядом.

from itertools import permutations
count = 0

# permutations('...', 4) создает перестановки (размещения без повторений).
# Это гарантирует выполнение условия "все цифры различны".
# Мы берем 4 цифры из 10 возможных.
for s in permutations('0123456789', 4):
    num = ''.join(s)
    
    # Число не может начинаться с нуля (иначе оно не четырехзначное)
    if num[0] != '0':
        # ХИТРЫЙ ПРИЕМ (Маска четности):
        # Заменяем все четные цифры (0, 4, 6, 8) на '2' (символ четности).
        # Цифру '2' не трогаем, она уже четная.
        num = num.replace('0', '2').replace('4', '2').replace('6', '2').replace('8', '2')
        
        # Заменяем все нечетные цифры (3, 5, 7, 9) на '1' (символ нечетности).
        # Цифру '1' не трогаем, она уже нечетная.
        num = num.replace('3', '1').replace('5', '1').replace('7', '1').replace('9', '1')
        
        # Теперь строка num состоит только из '1' и '2'.
        # Условие "никакие две чётные и две нечётные не стоят рядом" означает,
        # что не должно быть подстрок '11' (две нечетные подряд) и '22' (две четные подряд).
        if '11' not in num and '22' not in num:
            count += 1
           
print(f'Задача 10: {count}')
