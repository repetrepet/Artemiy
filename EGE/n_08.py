# Все 4-⁠буквенные слова, составленные из букв М, У, Х, А записаны в алфавитном порядке и пронумерованы.
#
# Вот начало списка:
# 1.АААА
# 2.АААМ
# 3.АААУ
# 4.АААХ
# 5.ААМА
#
# Напишите номер слова ХУХХ.

from itertools import product

# ВАЖНО: Порядок символов в строке alph должен соответствовать их "алфавитному" порядку в задаче.
# Из списка (АААА, АААМ, АААУ, АААХ) видно, что порядок букв: А -> М -> У -> Х.
alph = 'АМУХ'

# Используем enumerate, чтобы получать не только комбинацию, но и её порядковый номер.
# product(alph, repeat=4) генерирует слова в лексикографическом порядке, опираясь на порядок в alph.
for number, elemnt in enumerate(product(alph, repeat=4)):
    # elemnt — это кортеж букв, например ('Х', 'У', 'Х', 'Х').
    # ''.join(elemnt) склеивает их в строку 'ХУХХ' для сравнения.
    if ''.join(elemnt) == 'ХУХХ':
        # number начинается с 0, поэтому для ответа добавляем 1.
        print(f'Задача 8: {number + 1}')
        break


