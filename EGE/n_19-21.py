# Два игрока, Петя и Ваня, играют в следующую игру. Перед игроками лежит куча камней. Игроки ходят по очереди, первый ход делает Петя. За один ход игрок может добавить в кучу один камень либо увеличить количество камней в куче в два раза. Для того чтобы делать ходы, у каждого игрока есть неограниченное количество камней.

# Игра завершается в тот момент, когда количество камней в куче становится не менее 62.

# Победителем считается игрок, сделавший последний ход, т. е. первым получивший в куче 62 или больше камней.

# В начальный момент в куче было S камней; 1 ≤ S ≤ 61.

# Будем говорить, что игрок имеет выигрышную стратегию, если он может выиграть при любых ходах противника.

# Укажите такое значение S, при котором Петя не может выиграть за один ход, но при любом ходе Пети Ваня может выиграть своим первым ходом.

def f_single_pile_62(s, m):
    # Базовый случай: количество камней достигло 62 или более.
    if s >= 62:
        return m % 2 == 0
    
    if m == 0:
        return 0
    
    # Возможные ходы: +1 или *2.
    h = [
        f_single_pile_62(s + 1, m - 1),
        f_single_pile_62(s * 2, m - 1)
    ]
    
    return any(h) if m % 2 != 0 else all(h)


