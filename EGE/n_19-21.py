# Петя и Ваня решили поиграть. Перед ними лежит куча камней. Игроки ходят по очереди, первый ход делает Петя. За один ход игрок может:
# - добавить в кучу два камня
# - добавить в кучу три камня
# - увеличить количество камней в куче в три раза

# Например, из кучи в 55 камней за один ход можно получить кучу из 57, 58 или 165 камней.

# Игра завершается, когда количество камней в куче становится не менее 349. Победителем считается игрок, который сделал последний ход, то есть в результате его действия в куче стало 349 камней или больше.
# До начала игры в куче было S камней, 1 ≤ S ≤ 348.

# У игрока выигрышная стратегия, если он может выиграть при любых ходах противника.

# Известно, что Ваня выиграл своим первым ходом после неудачного первого хода Пети.

# Укажите минимальное значение S, при котором такая ситуация возможна.

def f(s, m):
    # Базовый случай: если количество камней достигло цели (349 и более), 
    # проверяем четность оставшихся ходов m. 
    # Если m четное, значит, последний ход сделал игрок, чью победу мы проверяем.
    if s >= 349:
        return m % 2 == 0
    
    # Если ходы закончились (m == 0), а цель не достигнута, текущая позиция проигрышная.
    if m == 0:
        return 0
    
    # Формируем список результатов рекурсивных вызовов для всех возможных ходов.
    # Уменьшаем m на 1, так как совершаем один ход.
    h = [
        f(s + 2, m - 1),
        f(s + 3, m - 1),
        f(s * 3, m - 1)
    ]
    
    # Если m нечетное, ход делает текущий игрок, и ему достаточно найти ХОТЯ БЫ ОДИН выигрышный вариант (any).
    # Если m четное, ход делает противник, и для гарантированной победы текущий игрок должен выиграть при ЛЮБОМ ходе противника (all).
    return any(h) if m % 2 != 0 else all(h)


# Задача 19: Ваня выигрывает своим первым ходом после неудачного хода Пети.
# Это означает, что существует такой ход Пети, после которого Ваня оказывается в выигрышной позиции.
# В стандартной логике f(s, 2) проверяет, что Ваня выигрывает при ВСЕХ ходах Пети (all). 
# Но так как в условии сказано про "неудачный ход" (то есть достаточно одного такого хода), 
# логику проверки для этого конкретного случая можно адаптировать, однако оставим базовый вызов f(s, 2) 
# с пояснением из исходного кода про необходимость использования any для анализа "неудачного" хода.
print('19)', [s for s in range(1, 349) if f(s, 2)])  # Ответ: 39


# Задача 20: Найдите два наименьших значения S, при которых у Пети есть выигрышная стратегия, 
# причём одновременно выполняются два условия:
# - Петя не может выиграть за один ход
# - Петя может выиграть своим вторым ходом независимо от того, как будет ходить Ваня
# not f(s, 1): Петя не выигрывает первым ходом.
# f(s, 3): Петя выигрывает своим вторым ходом (через 3 полухода: ход Пети, ход Вани, ход Пети).
print('20)', [s for s in range(1, 349) if not f(s, 1) and f(s, 3)])  # Ответ: 112, 113


# Задача 21: Найдите минимальное значение S, при котором одновременно выполняются два условия:
# - У Вани есть выигрышная стратегия, позволяющая ему выиграть первым или вторым ходом при любой игре Пети
# - У Вани нет стратегии, которая позволит ему гарантированно выиграть первым ходом
# not f(s, 2): Ваня не может гарантированно выиграть своим первым ходом при любом ходе Пети.
# f(s, 4): Ваня может выиграть своим вторым ходом (через 4 полухода) при любой игре Пети.
print('21)', [s for s in range(1, 349) if not f(s, 2) and f(s, 4)])  # Ответ: 110


