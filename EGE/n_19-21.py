# Перед игроками лежат две кучи камней
# За один ход игрок может добавить в одну из куч (по своему выбору) один камень или увеличить количество камней в куче в три раза.
# Игра завершается в тот момент, когда суммарное количество камней в кучах становится не менее 65.
# В начальный момент в первой куче было шесть камней, во второй куче — S камней; 1 ≤ S ≤ 58.

# Известно, что Ваня выиграл своим первым ходом после неудачного первого хода Пети. Укажите минимальное значение S, при котором такая ситуация возможна.

def f_two_piles_65(a, b, m):
    # Базовый случай: сумма камней в кучах достигла цели (65 и более).
    if a + b >= 65:
        return m % 2 == 0
    
    # Если ходы закончились, а цель не достигнута.
    if m == 0:
        return 0
    
    # Генерируем все 4 возможных хода: +1 или *3 для первой или второй кучи.
    h = [
        f_two_piles_65(a + 1, b, m - 1),
        f_two_piles_65(a * 3, b, m - 1),
        f_two_piles_65(a, b + 1, m - 1),
        f_two_piles_65(a, b * 3, m - 1)
    ]
    
    # any для поиска выигрышного хода текущим игроком, all для проверки неизбежности победы при ходе противника.
    return any(h) if m % 2 != 0 else all(h)


print('19)', [s for s in range(1, 59) if f_two_piles_65(6, s, 2)])  # Ответ: 7
print('20)', [s for s in range(1, 59) if not f_two_piles_65(6, s, 1) and f_two_piles_65(6, s, 3)])
print('21)', [s for s in range(1, 59) if not f_two_piles_65(6, s, 2) and f_two_piles_65(6, s, 4)])


