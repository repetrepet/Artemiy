# ЗАДАЧА 5
# =============================================================================
# Какая строка получится в результате применения приведённой ниже программы 
# к строке вида 1…12…2 (7 единиц, затем 7 двоек)? В ответе запишите 
# полученную строку.
#
# НАЧАЛО
# ПОКА нашлось (111) ИЛИ нашлось (222)
#     ЕСЛИ нашлось (111)
#         ТО заменить (111, 2)
#     КОНЕЦ ЕСЛИ
#     ЕСЛИ нашлось (222)
#         ТО заменить (222, 1)
#     КОНЕЦ ЕСЛИ
# КОНЕЦ ПОКА
# КОНЕЦ
# =============================================================================

# Исходная строка: семь единиц, затем семь двоек
s = '1' * 7 + '2' * 7

# Цикл пока есть "111" ИЛИ "222"
while '111' in s or '222' in s:
    # ПЕРВАЯ проверка: если есть "111"
    if '111' in s:
        # Заменяем три единицы на одну двойку
        # Это уменьшает количество единиц и добавляет двойки
        s = s.replace('111', '2', 1)
    # ВТОРАЯ проверка: если есть "222" (проверяется независимо!)
    if '222' in s:
        # Заменяем три двойки на одну единицу
        # Это уменьшает количество двоек и добавляет единицы
        s = s.replace('222', '1', 1)

# Выводим результат
print(s)


