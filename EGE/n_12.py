# ЗАДАЧА 2
# =============================================================================
# Какая строка получится в результате применения приведённой ниже программы 
# к строке, состоящей из цифры 1, за которой следуют 80 идущих подряд цифр 8? 
# В ответе запишите полученную строку.
#
# НАЧАЛО
#   ПОКА нашлось (18) ИЛИ нашлось (288) ИЛИ нашлось (3888)
#     ЕСЛИ нашлось (18)
#       ТО заменить (18, 2)
#       ИНАЧЕ ЕСЛИ нашлось (288)
#         ТО заменить (288, 3)
#         ИНАЧЕ заменить (3888, 1)
#       КОНЕЦ ЕСЛИ
#     КОНЕЦ ЕСЛИ
#   КОНЕЦ ПОКА
# КОНЕЦ
# =============================================================================

# Исходная строка: цифра 1, затем 80 восьмёрок
s = '1' + '8' * 80

# Цикл работает пока есть хотя бы одна из трёх подстрок
while '18' in s or '288' in s or '3888' in s:
    # ПЕРВЫЙ приоритет: если есть "18"
    if '18' in s:
        # Заменяем "18" на "2"
        # Это уменьшает количество восьмёрок
        s = s.replace('18', '2', 1)
    # ВТОРОЙ приоритет: если нет "18", но есть "288"
    elif '288' in s:
        # Заменяем "288" на "3"
        # Две восьмёрки после двойки превращаются в тройку
        s = s.replace('288', '3', 1)
    # ТРЕТИЙ приоритет: если нет предыдущих, но есть "3888"
    else:
        # Заменяем "3888" на "1"
        # Тройка с восьмёрками снова становится единицей
        s = s.replace('3888', '1', 1)

# Выводим финальную строку
print(s)


