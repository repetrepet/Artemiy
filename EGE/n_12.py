# Исполнитель Редактор получает на вход строку цифр и преобразовывает её. 
# Редактор может выполнять две команды, в обеих командах v и w обозначают 
# цепочки цифр.
#
# А) заменить (v, w).
# Эта команда заменяет в строке первое слева вхождение цепочки v на цепочку w. 
# Например, выполнение команды заменить (111, 27) преобразует строку 05111150 
# в строку 0527150. Если в строке нет вхождений цепочки v, то выполнение 
# команды заменить (v, w) не меняет эту строку.
#
# Б) нашлось (v).
# Эта команда проверяет, встречается ли цепочка v в строке исполнителя Редактор. 
# Если она встречается, то команда возвращает логическое значение «истина», 
# в противном случае возвращает значение «ложь». Строка исполнителя при этом 
# не изменяется.
#
# Цикл ПОКА условие ... КОНЕЦ ПОКА выполняется, пока условие истинно.
#
# В конструкции ЕСЛИ условие ТО команда1 ИНАЧЕ команда2 КОНЕЦ ЕСЛИ выполняется 
# команда1 (если условие истинно) или команда2 (если условие ложно).
#
# Какая строка получится в результате применения приведённой ниже программы 
# к строке, состоящей из 68 идущих подряд цифр 8? В ответе запишите 
# полученную строку.
#
# НАЧАЛО
# ПОКА нашлось (222) ИЛИ нашлось (888)
#     ЕСЛИ нашлось (222)
#         ТО заменить (222, 8)
#         ИНАЧЕ заменить (888, 2)
#     КОНЕЦ ЕСЛИ
# КОНЕЦ ПОКА
# КОНЕЦ
# =============================================================================

# Исходная строка: 68 восьмёрок подряд
s = '8' * 68

# Цикл выполняется пока в строке есть подстрока "222" ИЛИ подстрока "888"
while ('222' in s) or ('888' in s):
    # Проверяем условие: если найдена подстрока "222"
    if '222' in s: 
        # Заменяем ПЕРВОЕ (слева) вхождение "222" на "8"
        # Параметр 1 в replace означает замену только первого вхождения
        s = s.replace('222', '8', 1)
    else: 
        # Иначе (если "222" не найдено, но "888" есть)
        # Заменяем ПЕРВОЕ вхождение "888" на "2"
        s = s.replace('888', '2', 1)

# Выводим итоговую строку после всех преобразований
print(s)


