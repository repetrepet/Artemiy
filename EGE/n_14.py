# Задача 2
# Значение арифметического выражения 9^8 + 3^5 − 9 записали в системе счисления 
# с основанием 3. Сколько цифр 2 содержится в этой записи?
# ----------------------------------------------------------------------------
# Решение:
# 1. Вычисляем значение выражения в десятичной системе
# 2. Переводим в троичную систему делением на 3 с запоминанием остатков
# 3. Переворачиваем строку (остатки записываются в обратном порядке)
# 4. Считаем количество цифр '2'

x = 9 ** 8 + 3 ** 5 - 9
s = ''
while x != 0:
    s += str(x % 3)  # добавляем остаток от деления на 3
    x //= 3          # целочисленное деление на основание системы
s = s[::-1]          # разворачиваем строку для правильного порядка цифр

result_2 = s.count('2')
print(f"Задача 2. Количество цифр '2' в троичной записи: {result_2}")


