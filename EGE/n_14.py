#     M = 842x5₉,  N = 8x725₉
# В записи чисел переменной x обозначена неизвестная цифра из алфавита 
# девятеричной системы счисления.
# Определите наименьшее значение натурального числа A, при котором существует 
# такой x, что (M + A) кратно N.
# ----------------------------------------------------------------------------
# Решение:
# 1. Перебираем A от 1 до разумного предела (1000)
# 2. Для каждого A перебираем все возможные цифры x (0-8) в 9-ричной системе
# 3. Переводим M и N из 9-ричной в десятичную
# 4. Проверяем условие: (M + A) % N == 0
# 5. При первом подходящем A выводим результат

result_7 = None
for A in range(1, 1000):
    found = False
    for x in '012345678':  # цифры 9-ричной системы
        M = int(f'842{x}5', 9)   # M = 842x5₉ → десятичное
        N = int(f'8{x}725', 9)   # N = 8x725₉ → десятичное
        
