# В записи чисел переменной x обозначена неизвестная цифра из алфавита 
# 15-ричной системы счисления.
# Определите наименьшее значение x, при котором значение данного арифметического 
# выражения кратно 14. Для найденного значения x вычислите частное от деления 
# значения арифметического выражения на 14 и укажите его в ответе в десятичной 
# системе счисления.
# Алфавит 15-ричной СС: 0-9, A(10), B(11), C(12), D(13), E(14)
# ----------------------------------------------------------------------------
# Решение:
# 1. Перебираем все возможные значения x от 0 до 14 (в виде символов алфавита)
# 2. Формируем строки чисел с подставленным x
# 3. Переводим из 15-ричной в десятичную с помощью int(string, base)
# 4. Проверяем делимость суммы на 14
# 5. При первом подходящем x выводим частное и завершаем поиск

alph = '0123456789abcde'  # цифры 15-ричной системы (0-14)

result_6 = None
for x in alph:
    num1 = f'123{x}5'      # первое число: 123x5₁₅
    num2 = f'1{x}233'      # второе число: 1x233₁₅
    total = int(num1, 15) + int(num2, 15)
    
    if total % 14 == 0:
        result_6 = total // 14
        print(f"Задача 6. Найдено наименьшее x = '{x}' (десятичное: {int(x, 15) if x.isalpha() else x})")
        print(f"Задача 6. Частное от деления на 14: {result_6}")
        break


